<h3 class="mt-4">Commission Structure:</h3>

<p>As a valued partner in the Getlead Partner Program, you are eligible to receive
commissions on sales of Getlead CRM services and products. Commissions
are earned when a customer, referred by you, completes a payment for
Getlead services. The commission for each sale will be credited to your
account following the successful processing of the customer's payment.</p>

<h5>Payment Timeline:</h5>
<p>To ensure proper accounting and processing, payments of commissions will
be released 30 days after the subscription payment has been received and
processed by Getlead. This 30-day period allows for necessary verifications
and ensures compliance with financial regulations.</p>

<h5>Minimum Payout Amount:</h5>

<p>To streamline the payment process, the minimum payout amount for
commissions is set at INR 3000. This threshold must be met or exceeded in
your account before a payout request can be processed. This policy is in place
to ensure efficient handling of financial transactions and administrative ease.</p>

<h5>Payout Requests:</h5>

<p>As a participant in the Getlead Partner Program, you can submit requests for
payment of your accumulated commissions through our dedicated partner
portal. This portal provides a convenient and secure way for you to manage
your commissions, view your earnings, and request payouts. To request a
payout, simply log in to the partner portal, verify your earnings, and if the
minimum payout amount is met, submit a payout request following the
instructions provided.</p>

<h5>Payment Method and Currency:</h5>

<p>All commission payments will be made in the specified currency and through
the payment method chosen by you at the time of registering for the Getlead
Partner Program. We offer various payment methods to accommodate your
preferences. Please ensure your payment details are accurate and up-to-date
in the partner portal to avoid any delays in receiving your commissions</p>



